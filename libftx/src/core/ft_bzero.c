#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int				i;
	unsigned char	*ptrs;

	i = 0;
	ptrs = (unsigned char *)s;
	while (n > 0)
	{
		ptrs[i] = 0;
		i++;
		n--;
	}
}
